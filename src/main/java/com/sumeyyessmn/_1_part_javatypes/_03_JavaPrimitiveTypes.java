package com.sumeyyessmn._1_part_javatypes;

public class _03_JavaPrimitiveTypes {

    public static void main(String[] args) {
        //Primitive Types: 8 tanedir. null deger alamazsiniz
        //tam sayilar (4)
        byte b1 = 127;
        System.out.println("byte: "+b1);
        short s1 = 32767;
        System.out.println("short: "+s1);
        int i1 = 2464632;
        System.out.println("int: "+i1);
        long l1 = 35768437823L;
        System.out.println("long: "+l1);
        //virgullu sayilar (floating point)
        float f2 = 3.14f;
        System.out.println("float: "+f2);
        double d2 = 3.14;
        System.out.println("double: "+d2);
        //boolean
        boolean b3 = true;
        System.out.println("boolean: "+b3);
        //char
        char c4 = 'c';
        System.out.println("char: "+c4);



    }
}
